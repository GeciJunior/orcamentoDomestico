package orcamentoDomestico;

public class Usuario {
	private String nome;
	private String cpf;
	private int matricula;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCpf() {
		return cpf;
	}
	public int getMatricula() {
		return matricula;
	}
	
	
}
